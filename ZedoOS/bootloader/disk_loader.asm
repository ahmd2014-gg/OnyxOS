[bits 16]
[global]

disk_loader:
    ; تهيئة الدوال لتحميل النواة من القرص
    ; يمكن استخدام تقنيات مثل قراءة الأقراص باستخدام المقاطعات 0x13
    mov ah, 0x02         ; المقاطعة 0x13 - قراءة قطاع من القرص
    mov al, 1            ; عدد القطاعات المطلوب قراءتها
    mov ch, 0            ; الأسطوانة
    mov cl, 2            ; القطاع (يبدأ من 2 هنا كمثال)
    mov dh, 0            ; الرأس
    mov dl, 0x80         ; تحديد محرك الأقراص (القرص 0x80 هو القرص الأول)
    int 0x13             ; استدعاء المقاطعة لقراءة القطاع
    jmp $                ; الانتظار إذا كانت العملية ناجحة

times 510 - ($ - $$) db 0
dw 0xAA55
